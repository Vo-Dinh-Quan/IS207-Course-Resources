-- Tạo bảng THUE
CREATE TABLE THUE (
    MAHD VARCHAR(10),
    MAPHONG VARCHAR(10),
    NGAYTHUE DATE,
    NGAYTRA DATE,
    GIATHUE DECIMAL(10, 2),
